## Focus Timer

## Description: Implement a basic Pomodoro timer to encourage focus and breaks.
## How it works:
# Use the time library to count down work and break intervals.

import time

print("Start focusing for 25 minutes!")
time.sleep(1500) # 25 minutes
print("Time for a 5-minute break!")
time.sleep(300) # 5 minutes
print("Break over, back to work!")